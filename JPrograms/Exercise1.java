//the program will do gallon to liter conversion. 1 gallon = 3.7854 liters

package JPrograms;

public class Exercise1 {
    public static void main(String[] args) {
        double gallons;
        double liters;

        gallons = 10;
        liters = gallons * 3.7854;
        System.out.println("10 gallon is " + liters + " liters.");
    }
}
